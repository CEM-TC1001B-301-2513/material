print(type(345)) # int -> integer -> Número entero
print(type(12.5)) # float -> Número real
print(type(15j)) # complex -> Número complejo
print(type("Hola")) # str -> string -> Texto
print(type(True)) # bool -> Booleanos
print(type(False))
print(type((1,2,3))) # tuple -> conjunto ordenado e inmutable de datos
print(type([1,2,3])) # list -> conjunto ordenado y mutable de datos
print(type({"llave": "valor"})) # dict -> conjunto ordenado, nombrado y mutable de datos